package com.campus.cloudisk.common;

import lombok.Getter;

/**
 * 全局错误码枚举
 * <p>
 * 统一定义所有业务错误码，与 {@link Result} 配合使用。
 * <br/>
 * 编码规范：
 * <ul>
 *   <li>2xx — 成功</li>
 *   <li>4xx — 客户端错误（参数错误、权限不足等）</li>
 *   <li>5xx — 服务端错误</li>
 *   <li>1xxx — 用户模块业务错误</li>
 *   <li>2xxx — 文件模块业务错误</li>
 *   <li>3xxx — 分享模块业务错误</li>
 *   <li>4xxx — OSS/存储模块错误</li>
 * </ul>
 * </p>
 *
 * 使用示例：
 * <pre>
 *   // Controller 层
 *   return Result.fail(ErrorCode.FILE_NOT_FOUND);
 *
 *   // Service 层抛出业务异常
 *   throw new BusinessException(ErrorCode.STORAGE_QUOTA_EXCEEDED);
 * </pre>
 *
 * @author campus-cloud
 * @since 2024-01-01
 */
@Getter
public enum ErrorCode {

    // ===================================================================
    //  通用错误码（4xx / 5xx）
    // ===================================================================

    /** 请求成功 */
    SUCCESS(200, "success"),

    /** 请求参数错误（缺少必填项、格式不正确等） */
    PARAM_ERROR(400, "请求参数错误"),

    /** 未登录或 Token 失效，需要重新登录 */
    UNAUTHORIZED(401, "未登录或登录已过期，请重新登录"),

    /** 已登录但无权限执行该操作（如普通用户访问管理员接口） */
    FORBIDDEN(403, "无权限执行此操作"),

    /** 请求的资源不存在 */
    NOT_FOUND(404, "资源不存在"),

    /** 操作失败（通用，具体原因见 message） */
    OPERATION_FAILED(400, "操作失败"),

    /** 服务器内部错误（通常是未捕获的异常） */
    INTERNAL_ERROR(500, "服务器内部错误，请稍后重试"),

    // ===================================================================
    //  用户模块（1xxx）
    // ===================================================================

    /** 用户名已被注册 */
    USERNAME_EXISTS(1001, "用户名已被注册"),

    /** 邮箱已被注册 */
    EMAIL_EXISTS(1002, "邮箱已被注册"),

    /** 用户名或密码错误 */
    WRONG_CREDENTIALS(1003, "用户名或密码错误"),

    /** 账号已被禁用，联系管理员处理 */
    ACCOUNT_DISABLED(1004, "账号已被禁用，请联系管理员"),

    /** 用户不存在 */
    USER_NOT_FOUND(1005, "用户不存在"),

    /** 旧密码验证失败 */
    WRONG_OLD_PASSWORD(1006, "旧密码错误"),

    // ===================================================================
    //  文件模块（2xxx）
    // ===================================================================

    /** 文件不存在（已删除或路径错误） */
    FILE_NOT_FOUND(2001, "文件不存在或已被删除"),

    /** 文件已存在（同目录下重名） */
    FILE_ALREADY_EXISTS(2002, "同名文件已存在"),

    /** 文件类型不被允许（不在白名单中） */
    FILE_TYPE_NOT_ALLOWED(2003, "不支持该文件类型"),

    /** 单个文件超过最大限制 */
    FILE_SIZE_EXCEEDED(2004, "文件大小超出限制"),

    /** 用户存储配额已满 */
    STORAGE_QUOTA_EXCEEDED(2005, "存储空间不足，请清理文件后重试"),

    /** 目录不存在 */
    FOLDER_NOT_FOUND(2006, "目录不存在"),

    /** 目录已存在 */
    FOLDER_ALREADY_EXISTS(2007, "同名目录已存在"),

    /** 文件上传失败（OSS 异常） */
    FILE_UPLOAD_FAILED(2008, "文件上传失败，请重试"),

    /** 文件下载失败 */
    FILE_DOWNLOAD_FAILED(2009, "文件下载失败，请重试"),

    /** 文件秒传失败（MD5 不匹配） */
    FILE_INSTANT_UPLOAD_FAILED(2010, "秒传失败"),

    /** 无权操作该文件（不是文件所有者） */
    FILE_ACCESS_DENIED(2011, "无权操作该文件"),

    // ===================================================================
    //  分享模块（3xxx）
    // ===================================================================

    /** 分享链接不存在或已失效 */
    SHARE_NOT_FOUND(3001, "分享链接不存在或已失效"),

    /** 分享链接已过期 */
    SHARE_EXPIRED(3002, "分享链接已过期"),

    /** 分享提取码错误 */
    SHARE_CODE_WRONG(3003, "提取码错误"),

    // ===================================================================
    //  OSS / 存储模块（4xxx）
    // ===================================================================

    /** OSS 删除对象失败 */
    OSS_DELETE_FAILED(4001, "存储对象删除失败"),

    /** OSS 上传失败 */
    OSS_UPLOAD_FAILED(4002, "存储上传失败"),

    /** OSS 生成预签名 URL 失败 */
    OSS_PRESIGN_FAILED(4003, "生成访问链接失败");

    // ===================================================================

    /** 错误码 */
    private final int code;

    /** 错误描述（默认展示给前端） */
    private final String message;

    ErrorCode(int code, String message) {
        this.code    = code;
        this.message = message;
    }
}
